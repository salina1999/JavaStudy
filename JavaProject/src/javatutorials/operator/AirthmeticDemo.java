package javatutorials.operator;

public class AirthmeticDemo {

	public static void main(String[] args) {
		
		// 연산자(산술, 대입, 비교, 논리)
		
		//산술연산자
		
		// result의 값은 3
		int result = 1 + 2;
		System.out.println(result);
		
		// result의 값은 2
		result = result - 1;
		System.out.println(result);
		
		// result의 값은 4
		result = result * 2;
		System.out.println(result);
		
		// result의 값은 2
		result = result / 2;
		System.out.println(result);
		
		// result의 값은 10
		result = result + 8;
		System.out.println(result);
		
		// result의 값은 3
		result = result % 7;
		System.out.println(result);
		
	}

}
